class Transaksi {
    protected String deskripsi;
    protected int jumlah;

    public Transaksi(String deskripsi, int jumlah) {
        this.deskripsi = deskripsi;
        this.jumlah = jumlah;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public int getJumlah() {
        return jumlah;
    }
}
